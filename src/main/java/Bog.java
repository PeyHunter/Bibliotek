public class Bog {

    private String      titel;
    private String      genre;
    private int         antal = 0;
    private int         aarstal;

    public Bog () {}


    public String getTitel()
    {
        return getTitel();
    }

    public String getGenre()
    {
        return genre;
    }

    public int getAntal()
    {
        return antal;
    }

    public int getAarstal()
    {
        return aarstal;
    }






}
